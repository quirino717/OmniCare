void computeMotors(){
	int pwm_positive = motorsPWM[0];
	int pwm_negative = 0;
	if(pwm_positive < 0)
	{
		pwm_positive = 0;
		pwm_negative = abs(motorsPWM[0]);
	}

	if(pwm_positive > 255) pwm_positive = 255;
	if(pwm_negative > 255) pwm_negative = 255;

	TIM2->CCR1 = pwm_positive;
	TIM2->CCR2 = pwm_negative;



	pwm_positive = motorsPWM[1];
	pwm_negative = 0;
	if(pwm_positive < 0)
	{
		pwm_positive = 0;
		pwm_negative = abs(motorsPWM[1]);
	}

	if(pwm_positive > 255) pwm_positive = 255;
	if(pwm_negative > 255) pwm_negative = 255;

	TIM2->CCR3 = pwm_positive;
	TIM2->CCR4 = pwm_negative;



	pwm_positive = motorsPWM[2];
	pwm_negative = 0;
	if(pwm_positive < 0)
	{
		pwm_positive = 0;
		pwm_negative = abs(motorsPWM[2]);
	}

	if(pwm_positive > 255) pwm_positive = 255;
	if(pwm_negative > 255) pwm_negative = 255;

	TIM22->CCR1 = pwm_positive;
	TIM22->CCR2 = pwm_negative;
}

